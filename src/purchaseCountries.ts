export function generateLocales(
    purchaseCountries: PURCHASE_COUNTRY[],
): LOCALE[] {
    const locales = purchaseCountries.map(
        (purchaseCountry: PURCHASE_COUNTRY) => {
            return `${purchaseCountry.toLocaleLowerCase()}-${purchaseCountry}`;
        },
    ) as unknown as LOCALE[];
    return locales;
}
